<p>
Os lugares de atuação do profissional formado em Gastronomia são variados. Ele pode atuar em:<br />
<li>Restaurantes;</li>
<li>Bares;</li>
<li>Lanchonetes;</li>
<li>Buffets;</li>
<li>Indústrias, hospitais;</li>
<li>Clubes sociais;</li>
<li>Hotéis, entre outros.</li><br /><br />
Hoje existem até concursos públicos para profissionais da área.<br />

    Além disso, o chef não é a única função da profissão de Gastronomia.
O setor envolve as bebidas e a experiência de alimentação como um todo, o que requer várias posições de trabalho,
como auxiliares de cozinha, sommeliers, pessoas para executar a limpeza, entre outros.
O potencial de geração de empregos é enorme.<br />

<center><strong>Como se profissionalizar em Gastronomia</strong></center><br />
    Quem escolhe a profissão de Gastronomia precisa se formalizar para conseguir posições mais atraentes nas cozinhas.
Você pode escolher a formação com grau de bacharel ou de tecnólogo. O curso superior de bacharelado em Gastronomia
oferece noções humanísticas e técnicas ao profissional.<br />

     a formação em tecnólogo fornece, ao futuro gastrônomo, conhecimentos mais técnicos de cozinha,
gestão e administração de restaurantes e estabelecimentos com preparação culinária.<br /><br />

<strong><center>Quais as principais funções dos profissionais em Gastronomia?</strong></center><br />
<li>Chef de cozinha</li>

    O chef certamente é a ocupação mais conhecida dentro da área de Gastronomia.
Os chefs são responsáveis pela cozinha dos estabelecimentos e comandam todo o processo de preparação dos alimentos.
 Além disso, coordenam a equipe de cozinheiros e criam os pratos mais importantes ou de mais difícil preparo.<br /><br />

    O chef pode se especializar em diversos tipos de cozinhas, como panificação e confeitaria, por exemplo.
Se for esse o caso, ele é chamado de “Chef Pâtisserie”.<br /><br />

<li>Gestor de estabelecimentos culinários</li><br />

    O gestor de um restaurante, bar ou qualquer outro estabelecimento que possua trabalho culinário precisa ter boas noções
 de gastronomia. Mas, mais importante que isso, é ter conhecimentos de empreendedorismo, gestão de pessoal e financeira.<br/><br />

     gestor trabalha com a entrada e saída de mercadorias, o recrutamento e manutenção da equipe de cozinheiros,
chefs e outros colaboradores. É também responsabilidade dele o bom relacionamento com a clientela e a
administração do estabelecimento enquanto empresa.<br /><br />

<li>Encarregado da segurança alimentar</li><br />

    Estes profissionais da Gastronomia são responsáveis pela qualidade e pela fiscalização da data de validade dos
alimentos que entram, saem e são estocados no estabelecimento.<br />

Ele verifica com os fornecedores quais as melhores matérias-primas e fiscaliza constantemente os alimentos
entregues aos consumidores finais.<br /><br />

<strong><center>O salário do profissional de Gastronomia</strong></center><br />
    Embora o chef seja o profissional mais “conhecido” do mercado gastronômico, ele não necessariamente é o que ganha mais
pela função que desempenha.<br /><br />

    O profissional da Gastronomia pode atuar como autônomo, proprietário do próprio negócio ou ainda como empregado em
estabelecimentos culinários ou empresas de consultoria.<br /><br />

    O salário médio da profissão de Gastronomia, independentemente de sua função, varia de acordo com cada estado da
federação.<br /><br />

    As médias salariais mensais vão de um salário mínimo (aprox. R$ 970) a cerca de R$ 3.500. Quando falamos apenas em
chefs de cozinha, essa média passa a girar entre R$ 1.800 e R$ R$ 4.000, de acordo com cada estado.<br /><br />

    Dentre os profissionais da área, o consultor é o que apresenta a maior média salarial por sua função, com rendimentos
mensais médios entre R$ 2.500 e R$ 5.000. O ramo de estabelecimentos que paga melhor é o ramo hospitalar, seguido por
clubes sociais e pela rede hoteleira.<br /><br />

