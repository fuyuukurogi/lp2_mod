<p><br />

<center><strong>Kaiseki Ryori</strong></center><br />
Tradicionalmente, o Kaiseki Ryori é uma refeição servida logo antes de uma Chanoyu — ou cerimônia de chá.
Suas principais características são a simplicidade, a delicadeza dos sabores e o arranjo artístico dos elementos no
prato, que normalmente é composto por legumes, vegetais e peixes de cores, texturas e sabores que se complementam.
 Muito chique, não é mesmo?<br />

<center><strong>Takoyaki</strong></center><br />

Crocante por fora e macio por dentro, esse bolinho recheado com polvo e cebolinha é feito em uma chapa especial e
requer muita agilidade por parte do cozinheiro. A aparência é a de um bolinho de feijão ou de bacalhau bem redondinho,
 mas o sabor é simplesmente surpreendente.<br/>

<center><strong>Ramen</strong></center><br />
O Ramen já é famoso entre os fãs de animes e mangás. Essa sopa cujo preparo funciona mais ou menos como o
 de um macarrão instantâneo, tem como diferencial o fato de vir coberta com outros ingredientes, como pedaços
 de carne de porco, broto de bambu, espinafre e até mesmo bacon e ovo, mistura que fica super saborosa!<br />

<center><strong>Tempura</strong></center><br />

Supostamente, o Tempura foi trazido ao Japão por missionários jesuítas portugueses no início do século XVI,
na época da quaresma, em que os portugas fizeram um prato com camarões e legumes empanados para fugir da carne vermelha.
 Se a história é verdadeira ou não, não sabemos, mas fato é que esse prato foi adotado pelos japas e se transformou
 em um verdadeiro ícone da cozinha nipônica! Tanto é que hoje já há restaurantes especializados em Tempura pelas ruas
 do Japão, acredita?<br />

<strong><center>Nabe</strong></center><br />
No Brasil, o frescor da cozinha japonesa combina muito bem com o nosso calorão, mas também há outros pratos
japoneses além do sushi para esquentar barrigas no inverno do outro lado do mundo. Um deles é o Nabe, uma espécie de
sopa tradicionalmente servida sobre um fogo portátil — mais ou menos como o de fondue — que pode ser cozida diretamente
 à mesa. Temperado com missô e guarnecido com legumes, tofu e frutos do mar, o Nabe é a pedida certa para não perder o
  hábito de comer comida japa mesmo nos dias mais frios!<br />

<center><strong>Gyoza</strong></center></br>
Outra especialidade japonesa importada é a Gyoza, um delicioso pastelzinho originário da China,
recheado de carne de porco, repolho e cebolinha, cozido no vapor e depois dourado em óleo quente para ser servido
com molho de soja (claro!) e óleo de gergelim. Algumas diferenças entre a versão chinesa e a japonesa estão no nome
(em chinês a Gyoza se chama Jiaozi) e no tempero com alho, mais forte no prato japonês.<br />

<center><strong>Onigiri</strong></center><br />
Um lanche que deixa barrinhas de cereal e sanduíches no chinelo, o Onigiri é um bolinho de arroz que pode ser recheado
 com conserva de ameixas, ovas de salmão, atum e muitas outras possibilidades. Ele é levado pelos japoneses em suas
  sofisticadas lancheiras — conhecidas comobento — para serem apreciados com uma tirinha de alga sempre que a fome bate.
  Os mais artísticos são elaborados em formato de bichinhos e personagens, como a Hello Kitty e tantos outros admirados
   do outro lado do mundo.</p>
